-- Pessoa Jurídica (referência a CLIENTE)
CREATE TABLE PESSOA_JURIDICA (
    COD_CLIENTE INT PRIMARY KEY,
    CNPJ VARCHAR(18) UNIQUE NOT NULL,   
    FOREIGN KEY (COD_CLIENTE) REFERENCES CLIENTE(COD_CLIENTE)
);

