CREATE TABLE CARRO (
    COD_CARRO INT PRIMARY KEY,
    COD_COR INT,
    COD_MARCA INT,
    COD_TIPO INT,
    NOME VARCHAR(20),
    PRECO FLOAT,
    ANO VARCHAR(20),
    QTD_EM_ESTOQUE INT,
    FOREIGN KEY (COD_COR) REFERENCES COR(COD_COR),
    FOREIGN KEY (COD_MARCA) REFERENCES MARCA(COD_MARCA),
    FOREIGN KEY (COD_TIPO) REFERENCES TIPO(COD_TIPO)
);

